-- Create the probes table in Supabase
create table public.probes (
  id bigint generated by default as identity primary key,
  text text not null,
  name text not null default 'Anonymous',
  status text not null default 'pending' check (status in ('pending', 'approved', 'rejected')),
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now()
);

-- Enable Row Level Security
alter table public.probes enable row level security;

-- Allow public to insert new probes
create policy "Anyone can insert probes" on public.probes
  for insert with check (true);

-- Allow public to read approved probes
create policy "Anyone can read approved probes" on public.probes
  for select using (status = 'approved');

-- Allow reading all probes for admin (you'll implement auth later)
create policy "Allow admin to read all probes" on public.probes
  for select using (true);

-- Allow admin to update probe status
create policy "Allow admin to update probes" on public.probes
  for update using (true);

-- Allow admin to delete probes  
create policy "Allow admin to delete probes" on public.probes
  for delete using (true);

