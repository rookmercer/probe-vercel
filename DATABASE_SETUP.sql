-- Create updated_at trigger
create or replace function public.handle_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

create trigger handle_probes_updated_at
  before update on public.probes
  for each row
  execute function public.handle_updated_at();